// Enhanced language detection and SEO optimization
export const detectLanguage = (userAgent: string, acceptLanguage: string): string => {
  // Priority language detection for better SEO
  const urduIndicators = ['ur', 'urdu', 'pakistan', 'pk'];
  const arabicIndicators = ['ar', 'arabic', 'saudi', 'uae', 'qatar', 'kuwait'];

  const lowerAgent = userAgent.toLowerCase();
  const lowerAccept = acceptLanguage.toLowerCase();

  // Check for Urdu/Pakistani users
  if (urduIndicators.some(indicator => lowerAgent.includes(indicator) || lowerAccept.includes(indicator))) {
    return 'ur';
  }

  // Check for Arabic users
  if (arabicIndicators.some(indicator => lowerAgent.includes(indicator) || lowerAccept.includes(indicator))) {
    return 'ar';
  }

  // Default to English for better SEO
  return 'en';
};

